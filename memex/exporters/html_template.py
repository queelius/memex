        '  var result = await parseStreamResponse(response, assistContent, container);\n'
        '\n'
        '  /* Handle tool use rounds */\n'
        '  var rounds = 0;\n'
        '  while (result && result.type === "tool_use" && rounds < MAX_TOOL_ROUNDS) {\n'
        '    rounds++;\n'
        '\n'
        '    /* Add assistant message with tool_use to history */\n'
        '    librarianMessages.push({ role: "assistant", content: result.contentBlocks });\n'
        '\n'
        '    /* Execute the tool */\n'
        '    var toolResult = executeTool(result.toolUse);\n'
        '\n'
        '    /* Add tool_result to history */\n'
        '    librarianMessages.push({\n'
        '      role: "user",\n'
        '      content: [{\n'
        '        type: "tool_result",\n'
        '        tool_use_id: result.toolUse.id,\n'
        '        content: toolResult\n'
        '      }]\n'
        '    });\n'
        '\n'
        '    /* Create new assistant div for next response */\n'
        '    var nextDiv = appendChatMessage(container, "assistant", "");\n'
        '    assistContent = nextDiv.querySelector(".message-content");\n'
        '    container.scrollTop = container.scrollHeight;\n'
        '\n'
        '    /* Decide if this is the final forced round */\n'
        '    var nextBody = {\n'
        '      model: model,\n'
        '      max_tokens: 4096,\n'
        '      stream: true,\n'
        '      system: LIBRARIAN_SYSTEM_PROMPT,\n'
        '      messages: librarianMessages\n'
        '    };\n'
        '    if (rounds < MAX_TOOL_ROUNDS) {\n'
        '      nextBody.tools = [EXECUTE_SQL_TOOL];\n'
        '    }\n'
        '\n'
        '    var nextResponse = await fetch(endpoint, {\n'
        '      method: "POST",\n'
        '      headers: headers,\n'
        '      body: JSON.stringify(nextBody)\n'
        '    });\n'
        '\n'
        '    if (!nextResponse.ok) {\n'
        '      var errText2 = await nextResponse.text();\n'
        '      appendSystemMessage(container, "API error " + nextResponse.status + ": " + errText2);\n'
        '      return;\n'
        '    }\n'
        '\n'
        '    result = await parseStreamResponse(nextResponse, assistContent, container);\n'
        '  }\n'
        '\n'
        '  /* Add final text to history */\n'
        '  if (result && result.type === "text" && result.text) {\n'
        '    librarianMessages.push({ role: "assistant", content: result.text });\n'
        '  } else if (result && result.contentBlocks) {\n'
        '    librarianMessages.push({ role: "assistant", content: result.contentBlocks });\n'
        '  }\n'
        '}\n'
        '\n'
        '/**\n'
        ' * Parse an SSE stream response. Renders text progressively.\n'
        ' * Returns {type: "text", text, contentBlocks} or {type: "tool_use", toolUse, contentBlocks}.\n'
        ' */\n'
        'async function parseStreamResponse(response, assistContent, container) {\n'
        '  var reader = response.body.getReader();\n'
        '  var decoder = new TextDecoder();\n'
        '  var buffer = "";\n'
        '  var accumulated = "";\n'
        '  var toolUse = null;\n'
        '  var toolJsonStr = "";\n'
        '  var contentBlocks = [];\n'
        '  var currentBlockIndex = -1;\n'
        '\n'
        '  while (true) {\n'
        '    var chunk = await reader.read();\n'
        '    if (chunk.done) break;\n'
        '    buffer += decoder.decode(chunk.value, { stream: true });\n'
        '\n'
        '    var lines = buffer.split("\\n");\n'
        '    buffer = lines.pop();\n'
        '\n'
        '    for (var k = 0; k < lines.length; k++) {\n'
        '      var line = lines[k].trim();\n'
        '      if (!line.startsWith("data: ")) continue;\n'
        '      var data = line.substring(6);\n'
        '      if (data === "[DONE]") continue;\n'
        '      try {\n'
        '        var evt = JSON.parse(data);\n'
        '        if (evt.type === "content_block_start") {\n'
        '          currentBlockIndex = evt.index;\n'
        '          if (evt.content_block && evt.content_block.type === "tool_use") {\n'
        '            toolUse = { id: evt.content_block.id, name: evt.content_block.name, input: {} };\n'
        '            toolJsonStr = "";\n'
        '          }\n'
        '        } else if (evt.type === "content_block_delta" && evt.delta) {\n'
        '          if (evt.delta.type === "text_delta") {\n'
        '            accumulated += evt.delta.text;\n'
        '            assistContent.innerHTML = renderMarkdown(accumulated);\n'
        '            container.scrollTop = container.scrollHeight;\n'
        '          } else if (evt.delta.type === "input_json_delta") {\n'
        '            toolJsonStr += evt.delta.partial_json;\n'
        '          }\n'
        '        } else if (evt.type === "content_block_stop") {\n'
        '          if (toolUse && toolJsonStr) {\n'
        '            try { toolUse.input = JSON.parse(toolJsonStr); } catch (e) {}\n'
        '          }\n'
        '          if (toolUse) {\n'
        '            contentBlocks.push({ type: "tool_use", id: toolUse.id, name: toolUse.name, input: toolUse.input });\n'
        '          } else if (accumulated) {\n'
        '            contentBlocks.push({ type: "text", text: accumulated });\n'
        '          }\n'
        '        }\n'
        '      } catch (e) {\n'
        '        /* Skip unparseable SSE lines */\n'
        '      }\n'
        '    }\n'
        '  }\n'
        '\n'
        '  if (toolUse) {\n'
        '    /* Show a subtle indicator that a query is running */\n'
        '    if (accumulated) {\n'
        '      assistContent.innerHTML = renderMarkdown(accumulated + "\\n\\n*Running query...*");\n'
        '    } else {\n'
        '      assistContent.innerHTML = renderMarkdown("*Running query...*");\n'
        '    }\n'
        '    return { type: "tool_use", toolUse: toolUse, contentBlocks: contentBlocks };\n'
        '  }\n'
        '  return { type: "text", text: accumulated, contentBlocks: contentBlocks };\n'
        '}\n'
        '\n'
        '/**\n'
        ' * Execute a tool call. Only accepts execute_sql.\n'
        ' * Returns JSON string result.\n'
        ' */\n'
        'function executeTool(toolUse) {\n'
        '  if (toolUse.name !== "execute_sql") {\n'
        '    return JSON.stringify({ error: "Unknown tool: " + toolUse.name });\n'
        '  }\n'
        '  var sql = (toolUse.input.query || "").trim();\n'
        '  var firstWord = sql.split(/\\s/)[0].toUpperCase();\n'
        '  if (firstWord !== "SELECT" && firstWord !== "WITH" && firstWord !== "PRAGMA" && firstWord !== "EXPLAIN") {\n'
        '    return JSON.stringify({ error: "Only SELECT queries are allowed. Got: " + firstWord });\n'
        '  }\n'
        '  try {\n'
        '    var rows = query(sql);\n'
        '    if (rows.length > 50) {\n'
        '      rows = rows.slice(0, 50);\n'
        '      rows.push({ _truncated: true, _message: "Results truncated to 50 rows" });\n'
        '    }\n'
        '    return JSON.stringify(rows);\n'
        '  } catch (e) {\n'
        '    return JSON.stringify({ error: e.message });\n'
        '  }\n'
        '}\n'
        '\n'
        '/**\n'
        ' * Create and append a chat message div.\n'
        ' * @returns {HTMLElement} The created div\n'
        ' */\n'
        'function appendChatMessage(container, role, text) {\n'
        '  var div = document.createElement("div");\n'
        '  div.className = "message " + role;\n'
        '  var roleDiv = document.createElement("div");\n'
        '  roleDiv.className = "message-role";\n'
        '  roleDiv.textContent = role;\n'
        '  div.appendChild(roleDiv);\n'
        '  var contentDiv = document.createElement("div");\n'
        '  contentDiv.className = "message-content";\n'
        '  if (text) contentDiv.innerHTML = renderMarkdown(text);\n'
        '  div.appendChild(contentDiv);\n'
        '  container.appendChild(div);\n'
        '  return div;\n'
        '}\n'
        '\n'
        '/** Append a system/error message. */\n'
        'function appendSystemMessage(container, text) {\n'
        '  var div = document.createElement("div");\n'
        '  div.className = "message system";\n'
        '  div.innerHTML = \'<div class="message-role">system</div><div class="message-content" style="color: var(--danger);">\' + esc(text) + "</div>";\n'
        '  container.appendChild(div);\n'
