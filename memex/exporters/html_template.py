        '\n'
        '  var model = localStorage.getItem("memex_model") || "claude-sonnet-4-6";\n'
        '  var systemPrompt = localStorage.getItem("memex_system_prompt") || "";\n'
        '\n'
        '  /* Build message history from DB */\n'
        '  var dbMessages = query(\n'
        '    "SELECT role, content FROM messages WHERE conversation_id = ? ORDER BY created_at ASC",\n'
        '    [activeConvId]\n'
        '  );\n'
        '  var history = [];\n'
        '  for (var i = 0; i < dbMessages.length; i++) {\n'
