        '    chips.push({cls: cls, filter: "source", value: src, label: src});\n'
        '  }\n'
        '\n'
        '  /* Tag chips (top 10 by frequency) */\n'
        '  var tags = query("SELECT tag, count(*) AS n FROM tags GROUP BY tag ORDER BY n DESC LIMIT 10");\n'
        '  for (var j = 0; j < tags.length; j++) {\n'
        '    var tag = tags[j].tag;\n'
        '    var cls2 = activeFilters.tag === tag ? "filter-chip active" : "filter-chip";\n'
        '    chips.push({cls: cls2, filter: "tag", value: tag, label: "#" + tag});\n'
        '  }\n'
        '\n'
        '  /* Starred toggle */\n'
        '  var starCls = activeFilters.starred ? "filter-chip active" : "filter-chip";\n'
        '  chips.push({cls: starCls, filter: "starred", value: "", label: "starred"});\n'
