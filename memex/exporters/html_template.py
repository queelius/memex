        '      var filterValue = this.getAttribute("data-value");\n'
        '      if (filterType === "source") {\n'
        '        activeFilters.source = activeFilters.source === filterValue ? null : filterValue;\n'
        '      } else if (filterType === "tag") {\n'
        '        activeFilters.tag = activeFilters.tag === filterValue ? null : filterValue;\n'
        '      } else if (filterType === "starred") {\n'
        '        activeFilters.starred = !activeFilters.starred;\n'
        '      }\n'
        '      renderFilters();\n'
        '      loadConversations();\n'
        '    });\n'
        '    container.appendChild(span);\n'
        '  }\n'
