        '/**\n'
        ' * Render source chips, tag chips (top 10), and starred toggle.\n'
        ' * Click toggles activeFilters and reloads the conversation list.\n'
        ' *\n'
        ' * All user-provided strings are escaped via esc() before insertion\n'
        ' * to prevent XSS. The esc() function uses textContent assignment\n'
        ' * on a detached element, which is a standard safe escaping pattern.\n'
        ' */\n'
        'function renderFilters() {\n'
        '  var container = document.getElementById("filters");\n'
        '  var chips = [];\n'
        '\n'
        "  /* Source chips */\n"
        "  var sources = query(\"SELECT DISTINCT source FROM conversations WHERE source IS NOT NULL AND source != '' ORDER BY source\");\n"
