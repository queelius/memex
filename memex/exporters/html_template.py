"""HTML SPA template for self-contained memex conversation browser.

The template loads sql.js (Wasm) from CDN and can open a SQLite database
via URL parameter, fetch, or file picker. All CSS and JS are inlined
for single-file deployment.
"""


def get_template(schema_ddl: str = "") -> str:
    """Return the complete HTML5 SPA document as a string.

    Assembles the page from composable parts: CSS variables, layout,
    components, HTML structure, and JS modules.

    Parameters
    ----------
    schema_ddl : str
        Database schema DDL, embedded into the librarian chat system prompt.
    """
    return (
        '<!DOCTYPE html>\n<html lang="en">\n<head>\n'
        '<meta charset="utf-8">\n'
        '<meta name="viewport" content="width=device-width, initial-scale=1">\n'
        '<title>memex</title>\n'
        '<style>\n'
        + _css_variables()
        + _css_layout()
        + _css_components()
        + '</style>\n</head>\n<body>\n'
        + _html_structure()
        + '<script src="https://cdn.jsdelivr.net/npm/sql.js@1.14.0/dist/sql-wasm.js"></script>\n'
        + '<script>\n'
        + _js_core()
        + _js_ui()
        + _js_chat(schema_ddl)
        + _js_settings()
        + '</script>\n</body>\n</html>'
    )


