        '/**\n'
        ' * Lightweight markdown-to-HTML renderer.\n'
        ' * Handles fenced code blocks, inline code, headings, bold, italic,\n'
        ' * images, links, horizontal rules, unordered lists, and paragraphs.\n'
        ' * @param {string} text - Raw markdown text\n'
        ' * @returns {string} HTML string\n'
        ' */\n'
        'function renderMarkdown(text) {\n'
        '  if (!text) return "";\n'
        '\n'
        '  /* Escape HTML entities first */\n'
        '  text = text.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");\n'
        '\n'
        '  /* Extract fenced code blocks and replace with placeholders */\n'
        '  var codeBlocks = [];\n'
        '  text = text.replace(/```(\\w*)\\n([\\s\\S]*?)```/g, function(m, lang, code) {\n'
        '    var idx = codeBlocks.length;\n'
        '    var cls = lang ? \' class="language-\' + lang + \'"\' : "";\n'
        '    codeBlocks.push("<pre><code" + cls + ">" + code + "</code></pre>");\n'
        '    return "\\x00CB" + idx + "\\x00";\n'
        '  });\n'
        '\n'
        '  /* Extract inline code and replace with placeholders */\n'
        '  var inlineCodes = [];\n'
        '  text = text.replace(/`([^`\\n]+)`/g, function(m, code) {\n'
        '    var idx = inlineCodes.length;\n'
        '    inlineCodes.push("<code>" + code + "</code>");\n'
        '    return "\\x00IC" + idx + "\\x00";\n'
        '  });\n'
        '\n'
        '  /* Headings */\n'
        '  text = text.replace(/^#### (.+)$/gm, "<h4>$1</h4>");\n'
        '  text = text.replace(/^### (.+)$/gm, "<h3>$1</h3>");\n'
        '  text = text.replace(/^## (.+)$/gm, "<h2>$1</h2>");\n'
        '  text = text.replace(/^# (.+)$/gm, "<h1>$1</h1>");\n'
        '\n'
        '  /* Horizontal rules */\n'
        '  text = text.replace(/^---+$/gm, "<hr>");\n'
        '\n'
        '  /* Unordered lists */\n'
        '  text = text.replace(/^[*-] (.+)$/gm, "<li>$1</li>");\n'
        '  text = text.replace(/((?:<li>.*<\\/li>\\n?)+)/g, "<ul>$1</ul>");\n'
        '\n'
        '  /* Images: ![alt](url) */\n'
        '  text = text.replace(/!\\[([^\\]]*)]\\(([^)]+)\\)/g, function(m, alt, url) {\n'
        '    if (/^(https?:|data:image\\/|assets\\/)/.test(url)) return \'<img src="\' + escAttr(url) + \'" alt="\' + escAttr(alt) + \'" loading="lazy">\';\n'
        '    return m;\n'
        '  });\n'
        '\n'
        '  /* Links: [text](url) */\n'
        '  text = text.replace(/\\[([^\\]]+)]\\(([^)]+)\\)/g, function(m, text2, url) {\n'
        '    if (/^(https?:|mailto:|#)/.test(url)) return \'<a href="\' + escAttr(url) + \'" target="_blank">\' + text2 + \'</a>\';\n'
        '    return m;\n'
        '  });\n'
        '\n'
        '  /* Bold+italic: ***text*** */\n'
        '  text = text.replace(/\\*\\*\\*(.+?)\\*\\*\\*/g, "<strong><em>$1</em></strong>");\n'
        '\n'
        '  /* Bold: **text** */\n'
        '  text = text.replace(/\\*\\*(.+?)\\*\\*/g, "<strong>$1</strong>");\n'
        '\n'
        '  /* Italic: *text* */\n'
        '  text = text.replace(/\\*(.+?)\\*/g, "<em>$1</em>");\n'
        '\n'
        '  /* Paragraph breaks: double newline */\n'
        '  text = text.replace(/\\n\\n+/g, "</p><p>");\n'
        '\n'
        '  /* Single newlines to <br> (but not inside block elements) */\n'
        '  text = text.replace(/\\n/g, "<br>");\n'
        '\n'
        '  /* Wrap in paragraph if not starting with block element */\n'
        '  if (!/^\\s*<(h[1-4]|pre|ul|hr|p)/.test(text)) {\n'
        '    text = "<p>" + text + "</p>";\n'
        '  }\n'
        '\n'
        '  /* Restore inline code placeholders */\n'
        '  text = text.replace(/\\x00IC(\\d+)\\x00/g, function(m, idx) {\n'
        '    return inlineCodes[parseInt(idx)];\n'
        '  });\n'
        '\n'
        '  /* Restore code block placeholders */\n'
        '  text = text.replace(/\\x00CB(\\d+)\\x00/g, function(m, idx) {\n'
        '    return codeBlocks[parseInt(idx)];\n'
        '  });\n'
        '\n'
        '  return text;\n'
        '}\n'
