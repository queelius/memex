        'function buildChatRequest(provider, apiKey, model, systemPrompt, messages) {\n'
        '  var headers = { "Content-Type": "application/json" };\n'
        '  if (provider === "openai") {\n'
        '    if (apiKey) headers["Authorization"] = "Bearer " + apiKey;\n'
