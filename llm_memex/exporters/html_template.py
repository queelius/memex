        '}\n'
        '\n'
        '/** Extract text delta from a single SSE data line, branching on provider. */\n'
        'function extractDeltaText(provider, data) {\n'
        '  if (!data || data === "[DONE]") return "";\n'
        '  var evt;\n'
        '  try { evt = JSON.parse(data); } catch (e) { return ""; }\n'
        '  if (provider === "openai") {\n'
        '    if (evt.choices && evt.choices[0] && evt.choices[0].delta) {\n'
        '      return evt.choices[0].delta.content || "";\n'
        '    }\n'
        '    return "";\n'
        '  }\n'
        '  /* anthropic content_block_delta */\n'
        '  if (evt.type === "content_block_delta" && evt.delta && evt.delta.type === "text_delta") {\n'
        '    return evt.delta.text || "";\n'
        '  }\n'
        '  return "";\n'
        '}\n'
        '\n'
        'async function resumeConversation(userText) {\n'
        '  if (!db || !activeConvId) return;\n'
        '\n'
        '  var endpoint = localStorage.getItem("llm_memex_endpoint") || "";\n'
